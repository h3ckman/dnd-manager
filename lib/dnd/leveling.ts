export function spellSlotsForLevel(
  caster: CasterType,
  level: number,
): readonly number[] {
  const lvl = Math.max(1, Math.min(level, MAX_LEVEL)) - 1;
  if (caster === "full") return FULL_CASTER_SLOTS[lvl];
  if (caster === "half") return HALF_CASTER_SLOTS[lvl];
  if (caster === "third") return THIRD_CASTER_SLOTS[lvl];
  if (caster === "pact") {
    const [count, slotLevel] = PACT_SLOTS[lvl];
    const out = new Array(9).fill(0);
    out[slotLevel - 1] = count;
    return out;
  }
  return [0, 0, 0, 0, 0, 0, 0, 0, 0];
}
